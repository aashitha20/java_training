// narrowing typecasting
package javatraining;
public class typecast {
    public static void main(String[] args) {
        // narrowing typecasting
        float f=10.5F;
        int a=(int)f;
        System.out.println(a);  

        double d=20.5D;
        int b=(int)d;
        System.out.println(b);  
    }
}
